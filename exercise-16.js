function graduates (students) {
  var obj = {}
  var output = ""
  
  for ( var i=0; i<students.length; i++ ) {
      obj[students[i].class] = []
  }

  for ( key in obj ) {
      for ( var j=0; j<students.length; j++ ) {
          if( key==students[j].class && students[j].score > 75 ) {
            student = {}
            student.name = students[j].name,
            student.score = students[j].score
            obj[key].push(student)                                                        
          }
      }
      
  }
  output = obj
  return output
}

